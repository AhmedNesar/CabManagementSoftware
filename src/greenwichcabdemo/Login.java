    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        PreparedStatement st;
        ResultSet rs;
        
        String username = jTextField_UserName.getText();
        String pass = String.valueOf(jPasswordField_Password.getPassword());
        
        String query ="SELECT * FROM dispatchar WHERE username= ? AND password= ? ";
        
        try {
            st = DBConnection.getConnection().prepareStatement(query);
             st.setString(1, username);
             st.setString(2, pass);
             rs = st.executeQuery();
             
             if(rs.next())
             {
                 MyDispatcher form = new MyDispatcher();
                 form.setVisible(true);
                 form.pack();
                 form.setLocationRelativeTo(null);
                 
                 this.dispose();
                 
             }else{
                 JOptionPane.showMessageDialog(null,"Invalid Useraname or Password!","Login Failed!", 2);
             }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
    }//GEN-LAST:event_jButton_LoginActionPerformed

